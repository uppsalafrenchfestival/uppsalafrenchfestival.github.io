

function display_program()
{
    $('#type_layout').hide();
    $('#theme_layout').hide();
    $('#programme_layout').show();
}

function display_theme()
{
    $('#type_layout').hide();
    $('#programme_layout').hide();
    $('#theme_layout').show();
}

function display_type()
{
    $('#programme_layout').hide();
    $('#theme_layout').hide();
    $('#type_layout').show();
}

